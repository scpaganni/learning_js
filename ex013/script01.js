// O while é uma estrutura de repetição com com teste lógico no início

var contador = 1
while (contador <= 20) {
  console.log(`Numero ${contador}`)
  contador++
}

// O do while é uma estrutura de repetição com teste lógico no final

var contador = 5
do {
  console.log(`Número ${contador}`)
  contador++
} while (contador <= 30)